def to_simplified(text: str) -> str:
    if OPENCC is None:
        return text
    return OPENCC.convert(text)


def clean_japanese_field(text: str) -> str:
    text = text.replace("<", "く")
    text = text.replace("♪", "")
    text = text.strip()
    return text


def normalize_entry(path_tuple: tuple[int, int, int, int], entry: dict) -> dict:
    entry = dict(entry)

    for field in ("词条", "词条补充"):
        value = entry.get(field)
        if isinstance(value, str):
            value = clean_japanese_field(value)
            if field == "词条" and value in TERM_OVERRIDES:
                value = TERM_OVERRIDES[value]
            if field == "词条补充" and value in SUPPLEMENT_OVERRIDES:
                value = SUPPLEMENT_OVERRIDES[value]
            entry[field] = value

    explanation = entry.get("词语解释")
    if isinstance(explanation, str):
        explanation = to_simplified(explanation)
        explanation = EXPLANATION_OVERRIDES.get(explanation, explanation)
        entry["词语解释"] = explanation

    return entry


