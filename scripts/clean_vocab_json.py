def main() -> None:
    data = json.loads(VOCAB_PATH.read_text())

    for lesson_idx, lesson in enumerate(data["课次"], start=1):
        for unit_idx, unit in enumerate(lesson["单元"], start=1):
            for list_idx, word_list in enumerate(unit["词表"], start=1):
                cleaned_entries = []
                for entry_idx, entry in enumerate(word_list["词条列表"], start=1):
                    path_tuple = (lesson_idx, unit_idx, list_idx, entry_idx)
                    if path_tuple in BROKEN_REMOVALS and entry.get("词条") == BROKEN_REMOVALS[path_tuple]:
                        continue
                    cleaned_entries.append(normalize_entry(path_tuple, entry))
                word_list["词条列表"] = cleaned_entries

    for lesson in data["课次"]:
        for unit in lesson["单元"]:
            for word_list in unit["词表"]:
                word_list.pop("pdf页码", None)
                word_list.pop("教材页码", None)

    fix_special_lists(data)

    VOCAB_PATH.write_text(json.dumps(data, ensure_ascii=False, indent=2) + "\n")


if __name__ == "__main__":
    main()
